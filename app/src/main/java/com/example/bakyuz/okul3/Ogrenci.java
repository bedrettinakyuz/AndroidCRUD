package com.example.bakyuz.okul3;

public class Ogrenci {
    private  int ogrId;
    private String ogrAd;
    private String ogrSoyad;
    private  String ogrNumara;
    public Ogrenci(){


    }

    public int getOgrId() {
        return ogrId;
    }

    public void setOgrId(int ogrId) {
        this.ogrId = ogrId;
    }

    public String getOgrAd() {
        return ogrAd;
    }

    public void setOgrAd(String ogrAd) {
        this.ogrAd = ogrAd;
    }

    public String getOgrSoyad() {
        return ogrSoyad;
    }

    public void setOgrSoyad(String ogrSoyad) {
        this.ogrSoyad = ogrSoyad;
    }

    public String getOgrNumara() {
        return ogrNumara;
    }

    public void setOgrNumara(String ogrNumara) {
        this.ogrNumara = ogrNumara;
    }
}
